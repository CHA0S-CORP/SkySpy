// Generate many aircraft for pagination/virtual scrolling tests
export function generateManyAircraft(count = 100) {
  const aircraft = [];
  const types = ['B738', 'A320', 'B77W', 'A388', 'E190', 'B789', 'A321', 'CRJ9'];
  const airlines = ['UAL', 'DAL', 'AAL', 'SWA', 'AFR', 'BAW', 'KLM', 'DLH'];

  for (let i = 0; i < count; i++) {
    aircraft.push({
      hex: `${String(i).padStart(6, '0')}`,
      flight: `${airlines[i % airlines.length]}${String(100 + i).padStart(3, '0')}`,
      type: types[i % types.length],
      alt: Math.floor(Math.random() * 45000),
      gs: Math.floor(200 + Math.random() * 400),
      vr: Math.floor(-2000 + Math.random() * 4000),
      track: Math.floor(Math.random() * 360),
      distance_nm: Math.round((Math.random() * 100) * 10) / 10,
      rssi: -10 - Math.random() * 35,
      squawk: String(1000 + i).padStart(4, '0'),
      lat: 52 + Math.random() * 0.5,
      lon: 4 + Math.random() * 1,
      military: i % 20 === 0,
      emergency: i % 50 === 0,
      r: `N${String(10000 + i)}`,
    });
  }

  return aircraft;
}

